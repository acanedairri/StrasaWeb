package org.strasa.web.germplasmquery.view.model;

public class GermplasmSearchResultModel {

	int id;
	int gid;
	String germplasmname;
	String othername;
	String breeder;
	int germplasmtypeid;
	String irnumber;
	String ircross;
	String parentage;
	String femaleparent;
	String maleparent;
	String selectionhistory;
	String source;
	String remarks;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public String getGermplasmname() {
		return germplasmname;
	}
	public void setGermplasmname(String germplasmname) {
		this.germplasmname = germplasmname;
	}
	public String getOthername() {
		return othername;
	}
	public void setOthername(String othername) {
		this.othername = othername;
	}
	public String getBreeder() {
		return breeder;
	}
	public void setBreeder(String breeder) {
		this.breeder = breeder;
	}
	public int getGermplasmtypeid() {
		return germplasmtypeid;
	}
	public void setGermplasmtypeid(int germplasmtypeid) {
		this.germplasmtypeid = germplasmtypeid;
	}
	public String getIrnumber() {
		return irnumber;
	}
	public void setIrnumber(String irnumber) {
		this.irnumber = irnumber;
	}
	public String getIrcross() {
		return ircross;
	}
	public void setIrcross(String ircross) {
		this.ircross = ircross;
	}
	public String getParentage() {
		return parentage;
	}
	public void setParentage(String parentage) {
		this.parentage = parentage;
	}
	public String getFemaleparent() {
		return femaleparent;
	}
	public void setFemaleparent(String femaleparent) {
		this.femaleparent = femaleparent;
	}
	public String getMaleparent() {
		return maleparent;
	}
	public void setMaleparent(String maleparent) {
		this.maleparent = maleparent;
	}
	public String getSelectionhistory() {
		return selectionhistory;
	}
	public void setSelectionhistory(String selectionhistory) {
		this.selectionhistory = selectionhistory;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
}
